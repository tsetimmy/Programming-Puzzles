#include <iostream>
using namespace std;

void doit (cas) {
  int R, C, N;
  cin >> R >> C >> N;
  int g[R][C];

  for (int r = 0; r < R; r++)
    for (int c = 0; c < C; c++)
      g[r][c] = 0;




}

int main () {
  int tcases;
  cin >> tcases;
  for (int cas = 0; cas < tcases; cas++)
    doit(cas + 1);
  return 0;
}
